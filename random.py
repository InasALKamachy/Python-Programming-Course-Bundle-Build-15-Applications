from random import *

print(random()) # between 0.0 to 1.0
print(randint(-1,5)) # between two integer number positive or negative
print(uniform(0.1,0.9)) # between two float number
print(round(random(),2))
print(randrange(0,100,2)) # give begin, end, step , here print even number
print(randrange(1,101,2)) # give odd number
